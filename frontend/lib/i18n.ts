export type I18nKey = keyof typeof MESSAGES.zh

export function normalizeLang(raw?: string | null): Lang {
  if (!raw) return 'zh'
  const lower = raw.toLowerCase()
  if (lower.startsWith('en')) return 'en'
  return 'zh'
}

export function tr(lang: Lang, key: I18nKey, params?: Params): string {
  const template = MESSAGES[lang][key] ?? MESSAGES.zh[key]
  if (!params) return template
  return template.replace(/\{(\w+)\}/g, (_, k: string) => String(params[k] ?? `{${k}}`))
}

export function numberLocale(lang: Lang): string {
  return lang === 'en' ? 'en-US' : 'zh-CN'
}

