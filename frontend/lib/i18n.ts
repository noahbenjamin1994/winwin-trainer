export function translateBackendError(message: string, lang: Lang): string {
  if (lang === 'zh') return message
  if (BACKEND_ERROR_EXACT[message]) return BACKEND_ERROR_EXACT[message]
  for (const rule of BACKEND_ERROR_RULES) {
    const matched = message.match(rule.pattern)
    if (matched) return rule.map(matched)
  }
  return message
}
